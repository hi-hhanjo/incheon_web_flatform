"""다음 스포츠 팀 약칭 → 이 앱의 표시용 팀명 매핑.

다음 API는 팀을 약칭(shortNameKo: "인천", "전북", "울산" …)으로 준다. 순위 API·경기 API가
모두 이 약칭을 쓰므로, 앱 표기(data/*.json, lib/api/*.ts: "인천 유나이티드" 등)로 변환하는
단일 매핑을 둔다. 매핑에 없는 팀은 원문을 그대로 두어(조용히 버리지 않음) 검증에서 드러나게 한다.
"""

# 다음 약칭 → 앱 표기. 2026 K리그1 12팀 기준(2026-07-16 확인).
NORMALIZE_TEAM_NAME = {
    "서울": "FC서울",
    "전북": "전북 현대모터스",
    "강원": "강원FC",
    "포항": "포항 스틸러스",
    "울산": "울산 HD",
    "안양": "안양FC",
    "인천": "인천 유나이티드",
    "제주": "제주SK",
    "부천": "부천FC",
    "대전": "대전하나시티즌",
    "김천": "김천 상무",
    "광주": "광주FC",
}


def normalize_team_name(short_name: str) -> str:
    """다음 팀 약칭을 앱 표시용 팀명으로 바꾼다. 매핑에 없으면 원문 유지."""
    return NORMALIZE_TEAM_NAME.get(short_name.strip(), short_name.strip())
